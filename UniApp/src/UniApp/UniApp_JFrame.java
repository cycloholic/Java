    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Ελέγχουμε αν υπάρχει επιλεγμένο πανεπιστήμιο
        String selectedUniversity = jTextField2.getText();
        if (selectedUniversity == null || selectedUniversity.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Δεν υπάρχει επιλεγμένο πανεπιστήμιο.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Δημιουργούμε ένα transaction στην βάση.
        em.getTransaction().begin();
        //Ψάχνουμε για το συγκεκριμένο πανεπιστήμιο στη βάση
        Uniapp university = controller.findUniapp(selectedUniversity);

        if (university != null) {
            // Ενημερώνουμε τα στοιχεία του πανεπιστημίου με αυτά που έβαλε ο χρήστης
            university.setWebPage(jTextField3.getText());
            university.setState(jTextField4.getText());
            university.setCode(jTextField5.getText());
            university.setCountry(jTextField6.getText());
            university.setViews(Integer.parseInt(jTextField7.getText()));
            university.setPhone(jTextField8.getText());
            university.setComments(jTextField9.getText());

            try {
                //Με το edit αποθηκεύουμε τις αλλαγές
                controller.edit(university);
                //Με το commit οι αλλαγές αποθηκεύονται οριστικά
                em.getTransaction().commit();
                //Εκτυπώνουμε τα ανάλογα μηνύματα
                JOptionPane.showMessageDialog(this, "Οι αλλαγές αποθηκεύτηκαν επιτυχώς!", "Ενημέρωση", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                em.getTransaction().rollback();
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Σφάλμα κατά την αποθήκευση των αλλαγών.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Το πανεπιστήμιο δεν υπάρχει στη βάση δεδομένων.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

