    private void updateList(String jsonResponse) {
        //χρησιμοποιούμε την gson για να διαβάσουμε την json
        Gson gson = new Gson();
        //μετατρέπουμε την json σε jsonarray
        JsonArray jsonArray = gson.fromJson(jsonResponse, JsonArray.class);
        //δημιουργούμε μια λίστα με τα ονόματα των πανεπιστημίων
        List<String> universityNames;
        universityNames = new ArrayList<>();
        for (int i = 0; i < jsonArray.size(); i++) {
            JsonObject university = jsonArray.get(i).getAsJsonObject();
            //για κάθε αντικείμενο json αποθηκεύει το όνομα.
            if (university.has("name")) {
                universityNames.add(university.get("name").getAsString());
            }
        }
        //ενημερώνουμε την jlist1
        DefaultListModel<String> listModel = new DefaultListModel<>();
        int counter = 0;
        for (String name : universityNames) {
            listModel.addElement(name);
            counter++;

        }

        jList1.setModel(listModel);
        if (counter == 0) {
            JOptionPane.showMessageDialog(this, "Δεν βρέθηκε κανένα πανεπιστήμιο", "Ενημέρωση", JOptionPane.INFORMATION_MESSAGE);
        } else if (counter == 1) {
            JOptionPane.showMessageDialog(this, "Βρέθηκε " + counter + " πανεπιστήμιο", "Ενημέρωση", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Βρέθηκαν " + counter + " πανεπιστήμια", "Ενημέρωση", JOptionPane.INFORMATION_MESSAGE);
        }
    }

