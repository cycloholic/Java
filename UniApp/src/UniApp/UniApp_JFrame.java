    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //αποθηκεύουμε το όνομα του πανεπιστημίου σε ένα string
        String selectedUniversity = jList1.getSelectedValue();
        //αν ο χρήστης δεν έχει επιλέξει κάτι εμφανίζει μήνυμα λάθους
        if (selectedUniversity == null) {
            JOptionPane.showMessageDialog(this, "Παρακαλώ επιλέξτε ένα πανεπιστήμιο.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //ξεκινάμε ένα transaction στην βάση δεδομένων
        em.getTransaction().begin();
        //ελέγχουμε μέσω της findUniapp του JpaController αν υπάρχει το πανεπιστήμιο στην βάση δεδομένων
        Uniapp university = controller.findUniapp(selectedUniversity);
        //αν υπάρχει, ενημερώνουμε τις προβολές
        if (university != null) {
            university.setViews(university.getViews() + 1);

            try {
                //ενημερώνει την βάση καλώντας την controller.edit
                controller.edit(university);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            //ολοκληρώνει το transaction
            em.getTransaction().commit();
        } else {
            //αν δεν υπάρχει στην βάση μας κάνει κλήση στο api
            String urlToCall = "http://universities.hipolabs.com/search?name=" + selectedUniversity;
            try {
                //στέλνουμε ένα request
                Request request = new Request.Builder().url(urlToCall).build();
                Response response = client.newCall(request).execute();
                //αν το api επιστρέψει δεδομένα, τα αποθηκεύουμε στην βάση
                if (response.isSuccessful() && response.body() != null) {
                    //αποθηκεύουμε την απάντηση σε ένα string
                    String responseString = response.body().string();
                    //δημιουργούμε ένα αντικείμενο gson το οποίο χρησιμοποιείται για να μετατρέψουμε json σε αντικείμενα java
                    Gson gson = new Gson();
                    //μετατρέπουμε το responsestring σε jsonarray
                    JsonArray jsonArray = gson.fromJson(responseString, JsonArray.class);
                    //ελέγχουμε αν το jsonarray δεν είναι κενό
                    if (jsonArray.size() > 0) {
                        //επιλέγουμε το πρώτο αντικείμενο και το μετατρέπουμε σε jsonobject
                        JsonObject universityJson = jsonArray.get(0).getAsJsonObject();
                        //δημιουργούμε ένα νέο αντικείμενο που θα αποθηκευτεί στη βάση
                        //αν δεν υπάρχει πεδίο, αντικαθιστούμε με unknown
                        university = new Uniapp(
                                //αποθηκεύουμε το όνομα
                                universityJson.get("name").getAsString(),
                                //για τα υπόλοιπα πεδία ελέγχουμε πρώτα αν υπάρχει εγγραφή και αν δεν υπάρχει βάζουμε unknown
                                universityJson.has("web_pages") ? universityJson.get("web_pages").getAsJsonArray().get(0).getAsString() : "Unknown",
                                universityJson.has("state-province") && !universityJson.get("state-province").isJsonNull() ? universityJson.get("state-province").getAsString() : "Unknown",
                                universityJson.has("alpha_two_code") ? universityJson.get("alpha_two_code").getAsString() : "Unknown",
                                universityJson.has("country") ? universityJson.get("country").getAsString() : "Unknown",
                                universityJson.has("domains") ? universityJson.get("domains").getAsJsonArray().get(0).getAsString() : "Unknown",
                                //δίνουμε την τιμή 1 στα views
                                1
                        );

                        try {
                            //αποθηκεύουμε το πανεπιστήμιο στη βάση με try catch σε περίπτωση σφάλματος
                            controller.create(university);
                            em.getTransaction().commit();
                        } catch (Exception e) {
                            e.printStackTrace();
                            JOptionPane.showMessageDialog(this, "Σφάλμα κατά την αποθήκευση στη βάση δεδομένων.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
                //το απαραίτητο try catch για την σύνδεση στο api
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Σφάλμα κατά τη σύνδεση στο API.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            }
        }
        //εμφανίζουμε τα δεδομένα μας στα πεδία
        jTextField2.setText(university.getName());
        jTextField3.setText(university.getWebPage());
        jTextField4.setText(university.getState());
        jTextField5.setText(university.getCode());
        jTextField6.setText(university.getCountry());
        jTextField7.setText(String.valueOf(university.getViews()));
        jTextField8.setText(university.getPhone());
        jTextField9.setText(university.getComments());
    }//GEN-LAST:event_jButton2ActionPerformed

